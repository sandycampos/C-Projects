#include<stdlib.h>

/*
    Conversão de strings:
    atof() -> converte string para double
    atoi() -> converte string para inteiro
    atol() -> converte string para long

    Geração de sequência pseudo-aleatória:
    rand() -> gera número aleatório
    srand() -> inicializa o gerador de números aleatórios

    Gerenciamento de memória dinâmica:
    malloc() -> faz alocação de memória, mantendo o lixo na memória
    calloc() -> faz alocação de memória, zerando o espaço em memória
    free() -> libera a memória (desalocação)
    realoc() -> modifica o tamanho do espaço alocado em memória

    Pesquisa e ordenação:
    qsort() -> ordena os elementos de um array

    Aritmética de inteiros:
    abs() -> valor absoluto
    div() -> divisão inteira
*/