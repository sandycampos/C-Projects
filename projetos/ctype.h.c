#include<ctype.h>

/*
    Teste de tipos de caracteres:
    isalnum() -> verifica se o caractere é alfanumérico (abc749)
    isalpha() -> verifica se o caractere é alfabético (abcde)
    isdigit() -> verifica se o caractere é um dígito decimal (0123)
    ispunct() -> verifica se o caractere é uma pontuação (?!.)
    isspace() -> se é um espaço ( )
    isupper() -> é maiúsculo
    tolower() -> converte para minúsculo
    toupper() -> converte para maiúscula
*/